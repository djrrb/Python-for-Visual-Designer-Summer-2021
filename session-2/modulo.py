# a quick modulo example
# get a number
x = 14
print(x)
# use straight up division
# this will return a floating point number
print(x/2)

# use “floor division”
# how many times does the second number go into the first number
print(x//2)

# use the modulo, which provides the remainder of floor division. “If the second number goes into the first number, how many are left over?”
print(x%2)